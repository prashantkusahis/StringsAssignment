public class eInUmberella {
    public static void main(String[] args) {
        String str="Umberella";
        for(int i=0;i<str.length();i++){
            if(str.charAt(i)=='e'){
                System.out.println("The letter 'e' is present in the word Umberella at index : "+i);
            }
        }
    }
}
